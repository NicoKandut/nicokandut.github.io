export class Structure {
	x: number;
	y: number;
	ci: ColorId;
	id: string;

	constructor(x: number, y: number, ci: ColorId) {
		this.x = x;
		this.y = y;
		this.ci = ci;
		this.id = 'unset';
	}
}

export class CircleStructure extends Structure {
	r: number;

	constructor(x: number, y: number, r: number, ci: ColorId) {
		super(x, y, ci);
		this.r = r;
	}
}

export class RectStructure extends Structure {
	w: number;
	h: number;
	a: number;

	constructor(x: number, y: number, w: number, h: number, a: number, ci: ColorId) {
		super(x, y, ci);
		this.w = w;
		this.h = h;
		this.a = a;
	}
}

export class TriStructure extends Structure {
	r: number;
	a: number;

	constructor(x: number, y: number, r: number, a: number, ci: ColorId) {
		super(x, y, ci);
		this.r = r;
		this.a = a;
	}
}

export interface Scene {
	structure: Array<CircleStructure | RectStructure | TriStructure>;
}

const shapeToInt = (shape: CircleStructure | RectStructure | TriStructure): number => {
	if (shape instanceof CircleStructure) {
		return 0;
	} else if (shape instanceof RectStructure) {
		return 1;
	} else if (shape instanceof TriStructure) {
		return 2;
	} else {
		throw new Error('Invalid structure type');
	}
};

export const prepareScenes = (scenes: Record<SceneId, Scene>) => {
	const result: Record<SceneId, Scene> = {
		[SceneId.CABIN]: {
			structure: []
		},
		[SceneId.COAST]: {
			structure: []
		}
	};

	for (const key in scenes) {
		const id = key as unknown as SceneId;

		for (const s of scenes[id].structure) {
			if (s instanceof CircleStructure) {
				result[id].structure.push(new CircleStructure(s.x, s.y, s.r, s.ci));
			} else if (s instanceof RectStructure) {
				result[id].structure.push(new RectStructure(s.x, s.y, s.w, s.h, s.a, s.ci));
			} else if (s instanceof TriStructure) {
				result[id].structure.push(new TriStructure(s.x, s.y, s.r, s.a, s.ci));
			} else {
				throw new Error('Invalid structure type');
			}
		}
	}

	for (const scene of Object.values(result)) {
		let circleIndex = 0;
		let rectIndex = 0;
		let triIndex = 0;

		scene.structure.sort((a, b) => {
			if (a.ci === ColorId.GROUND9) {
				return 1;
			}

			const shapeDiff = shapeToInt(a) - shapeToInt(b);
			const colorDiff = a.ci - b.ci;
			const xDiff = a.x - b.x;
			const yDiff = a.y - b.y;

			if (shapeDiff !== 0) {
				return shapeDiff;
			} else if (Math.abs(xDiff) > 2) {
				return xDiff;
			} else if (Math.abs(yDiff) > 2) {
				return yDiff;
			} else {
				return colorDiff;
			}
		});

		for (const s of scene.structure) {
			if (s instanceof CircleStructure) {
				s.id = `circle_${circleIndex++}`;
			} else if (s instanceof RectStructure) {
				s.id = `rect_${rectIndex++}`;
			} else if (s instanceof TriStructure) {
				s.id = `tri_${triIndex++}`;
			}
		}
	}

	return result;
};

